#include<stdio.h>
#include<math.h>
int main()
{
    int n,temp,c=0,s=0,r;
    scanf("%d",&n);
    temp=n;
    while(temp!=0)
    {
        temp=temp/10;
        c++;
    }
    temp=n;
    while(temp!=0)
    {
        r=temp%10;
        s=s+pow(r,c);
        temp=temp/10;
        c--;
    }
    if(s==n)
    {
        printf("True");
    }
    else
    {
        printf("False");
    }
}